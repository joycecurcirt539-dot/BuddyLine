interface EmojiPickerProps {
    onSelect: (emoji: string) => void;
    onClose: () => void;
    className?: string;
}

export const EmojiPicker = ({ onSelect, onClose, className }: EmojiPickerProps) => {
    const { t } = useTranslation();
    const [activeCategory, setActiveCategory] = useState('smileys');
    const containerRef = useRef<HTMLDivElement>(null);
    const { reduceMotion } = usePerformanceMode();

    // Handle click outside to close
    useEffect(() => {
        const handleClickOutside = (event: MouseEvent) => {
            if (containerRef.current && !containerRef.current.contains(event.target as Node)) {
                onClose();
            }
        };
        document.addEventListener('mousedown', handleClickOutside);
        return () => document.removeEventListener('mousedown', handleClickOutside);
    }, [onClose]);

    const displayEmojis = EMOJI_DATA.find(cat => cat.id === activeCategory)?.emojis || [];

    return (
        <motion.div
            ref={containerRef}
            initial={reduceMotion ? false : { opacity: 0, scale: 0.9, y: 10 }}
            animate={{ opacity: 1, scale: 1, y: 0 }}
            exit={reduceMotion ? undefined : { opacity: 0, scale: 0.9, y: 10 }}
            className={clsx(
                "w-72 sm:w-80 h-[380px] glass rounded-[32px] shadow-2xl overflow-hidden flex flex-col z-[101]",
                className
            )}
        >
            {/* Header / Tabs */}
            <div className="p-4 bg-surface/40 border-b border-outline-variant/10">
                <div className="flex justify-between items-center px-1">
                    <div className="flex gap-1 overflow-x-auto scrollbar-hide pb-1">
                        {EMOJI_DATA.map((cat) => (
                            <button
                                key={cat.id}
                                type="button"
                                title={t(`emoji.category.${cat.id}`, cat.id)}
                                onClick={() => setActiveCategory(cat.id)}
                                className={clsx(
                                    "p-2 rounded-xl transition-all flex-shrink-0",
                                    activeCategory === cat.id
                                        ? "bg-primary text-on-primary shadow-lg shadow-primary/20"
                                        : "text-on-surface-variant/60 hover:bg-surface-container"
                                )}
                            >
                                {cat.icon}
                            </button>
                        ))}
                    </div>
                    <button
                        onClick={onClose}
                        type="button"
                        title={t('common.close', 'Close')}
                        className="p-2 ml-2 text-on-surface-variant/40 hover:text-on-surface transition-colors flex-shrink-0"
                    >
                        <X size={18} />
                    </button>
                </div>
            </div>

            {/* Grid */}
            <div className="flex-1 overflow-y-auto p-4 scrollbar-hide">
                <div className="grid grid-cols-7 gap-1">
                    {displayEmojis.map((emoji, i) => (
                        <button
                            key={`${activeCategory}-${i}`}
                            type="button"
                            title={emoji}
                            onClick={() => onSelect(emoji)}
                            className="text-2xl p-2 hover:bg-surface-container rounded-xl transition-transform active:scale-125 hover:scale-110"
                        >
                            {emoji}
                        </button>
                    ))}
                </div>
            </div>

            {/* Footer */}
            <div className="px-5 py-3 bg-surface/20 border-t border-outline-variant/5">
                <p className="text-[10px] font-black uppercase tracking-widest text-primary/60">
                    {t(`emoji.category.${activeCategory}`, activeCategory)}
                </p>
            </div>
        </motion.div>
    );
};
